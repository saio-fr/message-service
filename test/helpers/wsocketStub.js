var when = require('when');

/**
 * 	register: Promise function(url, fn, options)
 * 	publish: Promise function(url, args, kwargs)
 */
function stub(register, publish) {
  return function() {
    this.register = register;
    this.publish = publish;
    this.start = function() {
      return when.resolve();
    };
    this.stop = function() {
      return when.resolve();
    };
    this.unregister = function() {
      return when.resolve();
    };
  };
}

module.exports.stub = stub;
